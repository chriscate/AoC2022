pts_type = {'X': 1, 'Y': 2, 'Z': 3}

def score_it(move_pair):
    # a, b, c = rock, paper, scissors = x, y, z

    their_move = move_pair[0]
    my_move = move_pair[1]

    if their_move == 'A' and my_move == 'X': return 3
    elif their_move == 'A' and my_move == 'Y': return 6
    elif their_move == 'A' and my_move == 'Z': return 0
    elif their_move == 'B' and my_move == 'X': return 0
    elif their_move == 'B' and my_move == 'Y': return 3
    elif their_move == 'B' and my_move == 'Z': return 6
    elif their_move == 'C' and my_move == 'X': return 6
    elif their_move == 'C' and my_move == 'Y': return 0
    elif their_move == 'C' and my_move == 'Z': return 3

def choose_it(move_outcome_pair):
    # a, b, c = rock, paper, scissors = x, y, z
    # x, y, z = lose, draw, win

    their_move = move_outcome_pair[0]
    outcome = move_outcome_pair[1]

    if their_move == 'A' and outcome == 'X': return [their_move, 'Z']
    elif their_move == 'A' and outcome == 'Y': return [their_move, 'X']
    elif their_move == 'A' and outcome == 'Z': return [their_move, 'Y']
    elif their_move == 'B' and outcome == 'X': return [their_move, 'X']
    elif their_move == 'B' and outcome == 'Y': return [their_move, 'Y']
    elif their_move == 'B' and outcome == 'Z': return [their_move, 'Z']
    elif their_move == 'C' and outcome == 'X': return [their_move, 'Y']
    elif their_move == 'C' and outcome == 'Y': return [their_move, 'Z']
    elif their_move == 'C' and outcome == 'Z': return [their_move, 'X']

